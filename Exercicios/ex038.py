pnumero = float(input('Digite o primeiro numero que voce quer comparar: '))
snumero = float(input('Digite o segundo numero que voce quer comparar: '))

if pnumero > snumero:
    print('o primeiro numero que voce escolheu é maior que o segundo numero')
elif pnumero < snumero:
    print('O segundo numero que voce esolheu é maior que o primeiro numero')
else:
    print('Os numeros escolhidos são iguais.')