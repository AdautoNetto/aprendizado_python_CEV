n1 = int(input('Digite o primeiro numero: '))
n2 = int(input('Digite o segundo numero:'))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
print('A soma dos numeros é: {}' .format(s))
print('A multiplicação dos numeros é: {}' .format(m))
print('A divisão dos numeros é: {:.3f}' .format(d))
print('A divisão inteira dos numeros é: {}' .format(di))
print('A potencia dos numeros é: {}' .format (e))